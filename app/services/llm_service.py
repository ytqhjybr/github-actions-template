def get_llm_response(prompt: str, system_prompt: str = "Ты полезный ассистент.") -> str:
    """
    Заглушка LLM. Поиск по документам работает, ответ формируется имитацией.
    """
    if "фрагменты документов" in prompt:
        # Извлекаем фрагменты из промпта
        parts = prompt.split("Фрагменты документов:")
        if len(parts) > 1:
            fragments = parts[1].split("Ответ:")[0].strip()
            return f"На основе найденных фрагментов:\n{fragments}\n\n(Ответ сформирован заглушкой. Для реального LLM добавьте рабочий API-ключ.)"
    return "[ЗАГЛУШКА] Запрос получен. Для реального ответа подключите LLM."